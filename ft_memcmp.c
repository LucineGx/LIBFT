#include "libft.h"

int		memcmp(const void *s1, const void *s2, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (*(char *)(s1 + i) != *(char *)(s2 + i))
			return (*(char *)(s1 + i) - *(char *)(s2 + i));
		i += 1;
	}
	return (0);
}
