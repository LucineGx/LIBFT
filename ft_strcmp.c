int		ft_strcmp(const char *s1, const char *s2)
{
	int i;

	i = 0;
	while (s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i += 1;
	}
	if (!(s1[i]))
		return (0);
	else
		return (s1[i] - s2[i]);
}
